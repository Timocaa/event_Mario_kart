#include "../include/libft.h"

int	ft_isxdigit(int c)
{
	if (((char)c >= 48 && (char)c <= 57) || ((char)c >= 65 && (char)c <= 70)
		|| ((char)c >= 97 && (char)c <= 102))
		return (4096);
	else
		return (0);
}
