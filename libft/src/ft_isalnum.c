#include "../include/libft.h"

int	ft_isalnum(int c)
{
	if (((char)c >= 48 && (char)c <= 57) || ((char)c >= 65 && (char)c <= 90)
		|| ((char)c >= 97 && (char)c <= 122))
		return (8);
	else
		return (0);
}
